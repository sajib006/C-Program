/*LAB 1 Task 10:
Write a C program that prints the first
two even positive whole numbers.
(Do NOT use loop for this problem).
*/
#include <stdio.h>
int main()
{
int n1=1, n2=2, n3=3, n4=4;
if(n1%2 == 0)
{
printf("%d\n", n1);
}
if(n2 % 2 == 0)
{
printf("%d\n", n2);
}
if(n3 % 2 == 0)
{
printf("%d\n", n3);
}
if(n4%2 == 0)
{
printf("%d\n", n4);
}
return 0;
}

